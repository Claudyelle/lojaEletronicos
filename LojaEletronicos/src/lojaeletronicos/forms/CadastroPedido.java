 public void inserirTabela(){
        int tamanho = pedidos.size();
        String matrizTable[][] = new String[tamanho][7];
        
        for(int i = 0; i < tamanho; i++){
            matrizTable[i][0] = pedidos.get(i).numero;
            matrizTable[i][2] = pedidos.get(i).vendedor.informacoes();
            matrizTable[i][1] = pedidos.get(i).cliente.informacoes();
            matrizTable[i][3] = pedidos.get(i).item.codigo.nome;
            matrizTable[i][4] = pedidos.get(i).item.getQuant();
            matrizTable[i][5] = pedidos.get(i).item.getValor();
            matrizTable[i][6] = Double.toString(pedidos.get(i).getTotal());
        }
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
            matrizTable,
            new String [] {
               "Pedido", "Cliente", "Vendedor", "Produto", "Quantidade", "Valor", "TOTAL"
            }
        ));
 }

    
public void mostrarClientes(){ 
        int tamanho = clientes.size();
        matriz = new String[tamanho];
         for(int i = 0; i < tamanho; i++){
            matriz[i] = clientes.get(i).nome;
        }
    }
    public void mostrarVendedores(){
        int tam = vendedores.size();
        matriz = new String[tam];
        for(int i = 0; i< tam; i++){
            matriz[i] = vendedores.get(i).nome;
        }
    }
    public void mostrarProds(){
        int tam = produtos.size();
        matriz = new String[tam];
        for(int i = 0; i< tam; i++){
            matriz[i] = produtos.get(i).nome;
        }
    }

